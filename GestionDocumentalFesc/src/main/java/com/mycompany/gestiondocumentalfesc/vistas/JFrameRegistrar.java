    private void jButtonSeleccionarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarArchivoActionPerformed

        JFileChooser escoger = new JFileChooser();

        escoger.setFileSelectionMode(JFileChooser.FILES_ONLY);

        int estado = escoger.showOpenDialog(null);

        if(estado == JFileChooser.APPROVE_OPTION) {
            File archivo = escoger.getSelectedFile();
            if((archivo == null || (archivo.getName().equals("")))) {
                txtNombreArchivo.setText("...");
            }
            else {
                txtNombreArchivo.setText(archivo.getAbsolutePath());
            }
            File directorio = new File("C:\\archivos");
        
        
        if (!directorio.exists()) {
           directorio.mkdir();     
        }
            try {
                File pdfDes = new File(directorio.getAbsolutePath()+ "\\" +archivo.getName());
                copyFile(archivo,pdfDes);
                
            } catch (IOException ex) {
                    System.out.println("Error");
            }
        }   
    }//GEN-LAST:event_jButtonSeleccionarArchivoActionPerformed

